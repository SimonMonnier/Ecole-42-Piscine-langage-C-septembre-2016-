void	ft_print_comb(void)
{
	char	tb[3];

	tb[0] = '0';
	tb[1] = '1';
	tb[2] = '2';
	while (tb[0] <= '7' && tb[1] <= '8' && tb[2] <= '9')
	{
		if (tb[0] != tb[1] && tb[1] != tb[2])
		{
			ft_putchar(a);
			ft_putchar(b);
			ft_putchar(c);
			if (tb[0] != '7' || tb[1] != '8' || tb[2] != '9')
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			tb[2]++;
			if (tb[2] == '9' + 1)
				tb[2] = tb[1]++ + 1;
			if (tb[1] == '8' + 1)
				tb[1] = tb[0]++ + 1;
		}
	}
}
