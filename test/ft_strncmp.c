int							ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int						i;

	i = 0;
	while (s1 && n--)
	{
		if (s1[i] != s2[i])
			break ;
		i++;
	}
	return (s1[i] - s2[i]);
}
