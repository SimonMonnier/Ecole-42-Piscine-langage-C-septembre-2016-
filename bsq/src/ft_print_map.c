#include "ft.h"

void				ft_print_map(t_bsq *bsq)
{
	int				i;

	i = 0;
	while (bsq->map[i])
	{
		ft_putstr(bsq->map[i]);
		ft_putchar('\n');
		++i;
	}
	ft_putchar('\n');
}
