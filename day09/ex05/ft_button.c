int												ft_button(int i, int j, int k)
{
	if ((i >= k && j <= k) || (i <= k && j >= k))
		return (k);
	if ((i >= j && k <= j) || (i <= j && k >= j))
		return (j);
	if ((j >= i && k <= i) || (j <= i && k >= i))
		return (i);
	if (i == j && j == k)
		return (i);
	return (0);
}
