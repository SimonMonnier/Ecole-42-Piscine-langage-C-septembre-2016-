int		ft_check_vert(int argc, char **argv)
{
	int i;
	int y;
	int c;

	c = 0;
	i = 1;
	y = 2;
	while (i < argc)
	{
		if (argv[i][c] == argv[y][c] && i != y && argv[i][c] != 46)
			return (1);
		y++;
		if (y == 9)
		{
			c++;
			y = 2;
		}
		if (c == 8)
		{
			c = 0;
			i++;
		}
	}
	return (0);
}
