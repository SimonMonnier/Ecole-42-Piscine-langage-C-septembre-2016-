int		ft_check_linear(int argc, char **argv)
{
	int i;
	int y;
	int c;

	i = 1;
	while (i < argc)
	{
		y = 0;
		c = 0;
		while (c < 10)
		{
			if (y != c && argv[i][y] == argv[i][c] && argv[i][y] != 46)
				return (1);
			y++;
			if (y == 9)
			{
				c++;
				y = 0;
			}
		}
		i++;
	}
	return (0);
}
